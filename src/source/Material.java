	private static Map<String, Integer> __nameToId = new HashMap<>();

	public static final int __LENGTH_USEFUL = 913;
	public static final int __LENGTH = 950;

	private static String[] __idToName = new String[__LENGTH];

	public static void init() {
		try {
			for (Field field : Material.class.getDeclaredFields()) {
				String name = field.getName();
				name = name.toLowerCase();
				if (!name.startsWith("__") && !name.startsWith("$")) {
					Integer id = field.getInt(null);
					__nameToId.put(name, id);
					__idToName[id] = name;
				}
			}
		} catch (IllegalArgumentException e) {
			e.printStackTrace();
		} catch (IllegalAccessException e) {
			e.printStackTrace();
		}
	}

	private static final String removeSuffix(String s, String suffix) {
		if (s.endsWith(suffix)) {
			return s.substring(0, s.length() - suffix.length());
		}
		return s;
	}

	private static final String removePrefix(String s, String prefix) {
		if (s.startsWith(prefix)) {
			return s.substring(prefix.length(), s.length());
		}
		return s;
	}

	public static int get(String name) {
		name = removePrefix(name, "minecraft:");
		if (__nameToId == null) {
			return _UNKOWN;
		}
		Integer result = __nameToId.get(name);
		if (result == null) {
			Loggger.log("cannot find material " + name);
			return _UNKOWN;
		}
		return result;
	}

	public static String getName(int id) {
		if (id >= Material.__LENGTH || id < 0) {
			return "unkown texture id " + id;
		}
		return __idToName[id];
	}
